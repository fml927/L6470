u16 Flash_CRC16(u8 *Frame,u8 Length)     // 传入要校验的数组名及其长度
{
  u8 CRCHi = 0xFF;
  u8 CRCLo = 0xFF;
  int iIndex;
  while( Length-- )
  {
    iIndex = CRCLo ^ *( Frame++ );
    CRCLo = ( u8 )( CRCHi ^ Flash_aucCRCHi[iIndex] );
    CRCHi = Flash_aucCRCLo[iIndex];
  }
  return ( u16 )( CRCHi << 8 | CRCLo );// CRC校验返回值
}
/****************************************************************************
* 名称：    Flash_Delay 
* 功能：    延时
* 入口参数：dly - 延时时间
出口参数：
****************************************************************************/
void Flash_Delay(u32 dly)
{
  u8 i;
  while(dly--)
  {
    i = 8;
    while(--i);
  }
}

/****************************************************************************
* 名称：    Flash_Read_Byte(u32 addr,u8 len,u8 *xp) 
* 功能：    按字面读取FLASH
* 入口参数：addr - flash的地址
*           len  - 数据(字节)长度
* 出口参数：*xp  - 读取的数据存放地址
****************************************************************************/
void Flash_Read_Byte(u32 addr,u16 len,u8 *xp) 
{ 
  u16 xi;
  u8 tmp8 = 0;
  u8 *temp_addr = (u8 *)addr;
  
  for(xi = 0; xi < len; xi++)
  {
    tmp8 = *temp_addr++;
    xp[xi] = tmp8;
  }
}

/****************************************************************************
* 名称：      Flash_Read_HalfWord(u32 addr,u8 len,u8 *xp) 
* 功能：      按半字(16位)读取FLASH
* 入口参数：  addr - flash的地址
*             len  - 数据(16位)长度
* 出口参数：  *xp  - 读取的数据存放地址
****************************************************************************/
void Flash_Read_HalfWord(u32 addr,u16 len,u16 *xp) 
{ 
  u16 xi;
  u16 tmp16 = 0;
  u16 *temp_addr = (u16 *)addr;
  
  for(xi = 0; xi < len; xi++)
  {
    tmp16 = *temp_addr;
    temp_addr ++;
    xp[xi] = tmp16;
  }
}

/****************************************************************************
* 名称：    Flash_Write_HalfWord(u32 addr,u16 len,u16 *xp) 
* 功能：    按半字(16位)向flash写入len个数据
* 入口参数：addr - flash地址,32位
*           len  - 数据(16位)长度, len <= 512
*           *xp  - 输出的数据地址
* 出口参数：0=数据写入成功,1=数据写入错误
****************************************************************************/
u8 Flash_Write_HalfWord(u32 addr,u16 len,u16 *xp) 
{
  u32 xi;
  u32 temp_addr;
  FLASH_Status statusa;
  
  if(len == 0)
  {   return(1);    }
  if(len > 512) 
  {   len = 512;    }
  
  temp_addr = addr;
  for(xi = 0; xi < len; xi++) 
  {   
    statusa = FLASH_ProgramHalfWord(temp_addr,(u16)xp[xi]); 	
    Flash_Delay(10);
    if( statusa != FLASH_COMPLETE)
    {
      return 1;
    }
    temp_addr += 2;
  }	
  return 0;      
  
}


/****************************************************************************
* 名称：    Flash_Read_Param(void)  
* 功能：    从FLASH中读取数到BUF
* 入口参数: offsetaddr - 偏移地址(从0开始,0=FLASH_DATA_BASE_ADDR)
*            buf - 8位数据指针
*            len - 数据(8位)长度(1~1022)      
* 出口参数：CRC校验的结果：0=CRC检验正确,1=CRC错误
* 说明：    自动对附加到数据包后面的CRC进行检验
****************************************************************************/
u8 Flash_Read_Param(u8 offsetaddr, u8 *buf, u16 len) 
{ 
  u16 tmp16;
  
  if(len % 2) len++;  
  if(len > 1022)
  {
    len = 1022;
  }
  Flash_Read_HalfWord(FLASH_DATA_BASE_ADDR + (1024 * offsetaddr), len / 2, (u16*)buf);	
  //计算CRC
  Flash_Read_HalfWord(FLASH_DATA_BASE_ADDR + (1024 * offsetaddr) + len, 1, &tmp16);	 
  
  if( Flash_CRC16((u8 *)buf, len) != tmp16)
  {
    return 1;
  }
  return 0;
}

/****************************************************************************
* 名称：    Flash_Save_Param(void)  
* 功能：    保存数据到FLASH
* 入口参数: offsetaddr - 偏移地址(从0开始,0=FLASH_DATA_BASE_ADDR)
*            buf - 8位数据指针
*            len - 数据(8位)长度(1~1022)      
* 出口参数：0=数据保存正确且CRC检验正确,1=CRC错误,2=FLASH操作异常
* 说明：    1.函数自动增加CRC检验功能
            2.对保存的数据回读比较。
****************************************************************************/
u8 Flash_Save_Param(u8 offsetaddr, u8 *buf, u16 len) 
{ 
  FLASH_Status statusa;
  u16 tmp16,i;
  u8  tmp8;
  u8 *tmpbuf;
  
  if(len % 2) len++;
  if(len > 1022)
  {
    len = 1022;
  }
  FLASH_Unlock();	 			// unlock Flash
  statusa = FLASH_ErasePage(FLASH_DATA_BASE_ADDR + (1024 * offsetaddr));		// earse FLASH_DATA_BASE_ADDR_A
  if( statusa != FLASH_COMPLETE)
  {
    FLASH_Lock();
    return 2;
  }
  
  tmp8 = Flash_Write_HalfWord(FLASH_DATA_BASE_ADDR + (1024 * offsetaddr), len / 2, (u16*)buf );
  //计算CRC
  tmp16 = Flash_CRC16((u8 *)buf, len);
  tmp8 |= Flash_Write_HalfWord(FLASH_DATA_BASE_ADDR + (1024 * offsetaddr) + len, 1, &tmp16 );
  
  FLASH_Lock();		//writting completed,lock flash
  if(tmp8)  //数据保存不成功，则退出程序，不进行下面的数据检查操作
  {
    return(2);
  }
  //检查数据写入是否正确
  tmpbuf = (u8 *)malloc(len+4);     //分配内存
  if(tmpbuf != NULL)                //内存分配成功
  {
    tmp8 = Flash_Read_Param(offsetaddr, tmpbuf, len);     //回读刚才写入的数据
    if(tmp8 == 0)   //回读的数据,CRC校验正确
    {
      for(i = 0; i < len; i++)    //按字节比较刚写入的数据是否一致
      {
        if(tmpbuf[i] != buf[i])   //数据不一致，则中断比较
        {
          tmp8 = 1;
          break;
        }
      }
    }
    free(tmpbuf);         //释放内存
  }
  return tmp8;
}